CREATE TABLE APPLICATION(
APP_ID  BIGINT  PRIMARY KEY AUTO_INCREMENT,
APP_NAME    VARCHAR(50) NOT NULL,
APP_DESCRIPTION VARCHAR(4000) NOT NULL,
SIZE    VARCHAR(20) NOT NULL,
CATEGORY_CODE   VARCHAR(50) NOT NULL,
PRICE   FLOAT(10,2) NOT NULL,
CONTENT_RATING  VARCHAR(20) NOT NULL,
UPDATE_DATE DATE,
CURRENT_VERSION VARCHAR(10) NOT NULL,
AD_FREQ_PHR INT NOT NULL,
RELEASED_DATE   DATE NOT NULL,
DEVELOPER_ID    BIGINT NOT NULL,
APP_ENGAGE_TIME_PD  INT NOT NULL,
INAPP_PURCHASE  TINYINT(1) DEFAULT 0,
EDITOR_CHOICE   TINYINT(1),
TYPE VARCHAR(10) NOT NULL,
FOREIGN KEY (CATEGORY_CODE) REFERENCES CATEGORY(CATEGORY_CODE),
FOREIGN KEY (DEVELOPER_ID) REFERENCES DEVELOPER(DEVELOPER_ID));
